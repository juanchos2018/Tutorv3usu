package com.example.tutorv3usu.Clases;

public class Alu {

    String id;
    String nombre;

    public Alu(String id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public Alu(){

    }



    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
